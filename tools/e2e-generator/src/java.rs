//! Java test generator for html-to-markdown e2e tests.
//!
//! Generates `e2e/java/src/test/java/dev/kreuzberg/e2e/{Category}Test.java` files.

use crate::fixtures::Fixture;
use anyhow::Result;
use camino::Utf8Path;
use itertools::Itertools;
use std::collections::HashMap;
use std::fmt::Write as FmtWrite;

/// Generate Java e2e tests from fixtures into `output_dir/java/`.
pub fn generate(fixtures: &[Fixture], output_dir: &Utf8Path) -> Result<usize> {
    let java_dir = output_dir.join("java");
    let test_java_dir = java_dir.join("src/test/java/dev/kreuzberg/e2e");
    std::fs::create_dir_all(&test_java_dir)?;

    // Group fixtures by category.
    let by_category: HashMap<&str, Vec<&Fixture>> = fixtures
        .iter()
        .filter(|f| !f.is_skipped_for("java"))
        .into_group_map_by(|f| f.resolved_category());

    let mut total = 0;

    for (category, category_fixtures) in &by_category {
        let class_name = category_to_class_name(category);
        let file_name = format!("{}Test.java", class_name);
        let path = test_java_dir.join(&file_name);
        let content = render_test_file(&class_name, category, category_fixtures);
        std::fs::write(&path, content)?;
        total += category_fixtures.len();
    }

    // Generate pom.xml.
    let pom_xml = render_pom_xml();
    std::fs::write(java_dir.join("pom.xml"), pom_xml)?;

    Ok(total)
}

