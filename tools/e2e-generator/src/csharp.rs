//! C# test generator for html-to-markdown e2e tests.
//!
//! Generates `e2e/csharp/{Category}Tests.cs` using xUnit.

use crate::fixtures::Fixture;
use anyhow::Result;
use camino::Utf8Path;
use itertools::Itertools;
use std::collections::HashMap;
use std::fmt::Write as FmtWrite;

/// Generate C# e2e tests from fixtures into `output_dir/csharp/`.
pub fn generate(fixtures: &[Fixture], output_dir: &Utf8Path) -> Result<usize> {
    let csharp_dir = output_dir.join("csharp");
    std::fs::create_dir_all(&csharp_dir)?;

    // Group fixtures by category.
    let by_category: HashMap<&str, Vec<&Fixture>> = fixtures
        .iter()
        .filter(|f| !f.is_skipped_for("csharp"))
        .into_group_map_by(|f| f.resolved_category());

    let mut total = 0;

    for (category, category_fixtures) in &by_category {
        let class_name = capitalize_category(category);
        let file_name = format!("{}Tests.cs", class_name);
        let path = csharp_dir.join(&file_name);
        let content = render_test_file(&class_name, category_fixtures);
        std::fs::write(&path, content)?;
        total += category_fixtures.len();
    }

    Ok(total)
}

fn render_test_file(class_name: &str, fixtures: &[&Fixture]) -> String {
    let mut out = String::new();
    let _ = writeln!(out, "// E2e tests for category: {}", class_name.to_lowercase());
    let _ = writeln!(
        out,
        "// Generated by html-to-markdown-e2e-generator — do not edit manually."
    );
    let _ = writeln!(out);
    let _ = writeln!(out, "#nullable enable");
    let _ = writeln!(out);
    let _ = writeln!(out, "using Xunit;");
    let _ = writeln!(out, "using HtmlToMarkdown;");
    let _ = writeln!(out);
    let _ = writeln!(out, "namespace HtmlToMarkdown.Tests;");
    let _ = writeln!(out);
    let _ = writeln!(out, "public class {class_name}Tests");
    let _ = writeln!(out, "{{");
    let _ = writeln!(out);

    for fixture in fixtures {
        render_test_function(&mut out, fixture);
        let _ = writeln!(out);
    }

    let _ = writeln!(out, "}}");

    out
}

fn render_test_function(out: &mut String, fixture: &Fixture) {
    let method_name = sanitize_method_name(&fixture.id);
    let description = &fixture.description;

    let _ = writeln!(out, "    /// <summary>");
    let _ = writeln!(out, "    /// {description}");
    let _ = writeln!(out, "    /// </summary>");
    let _ = writeln!(out, "    [Fact]");
    let _ = writeln!(out, "    public void Test{method_name}()");
    let _ = writeln!(out, "    {{");

    // HTML input.
    let html = match &fixture.html {
        Some(html) => html.as_str(),
        None => {
            let _ = writeln!(out, "        // SKIP: no inline html");
            let _ = writeln!(out, "    }}");
            return;
        }
    };

    let escaped_html = escape_csharp_string(html);
    let _ = writeln!(out, "        var html = \"{escaped_html}\";");

    // Conversion call + error handling.
    if fixture.assertions.expect_error == Some(true) {
        let _ = writeln!(
            out,
            "        var exception = Assert.Throws<Exception>(() => HtmlToMarkdownConverter.Convert(html));"
        );
        if let Some(contains) = &fixture.assertions.error_contains {
            let escaped = escape_csharp_string(contains);
            let _ = writeln!(out, "        Assert.Contains(\"{escaped}\", exception.Message);");
        }
        let _ = writeln!(out, "    }}");
        return;
    }

    let _ = writeln!(out, "        var result = HtmlToMarkdownConverter.Convert(html);");
    let _ = writeln!(out, "        var content = result.Content ?? \"\";");
    let _ = writeln!(out);

    let a = &fixture.assertions;

    // content_equals
    if let Some(expected) = &a.content_equals {
        let escaped = escape_csharp_string(expected);
        let _ = writeln!(out, "        Assert.Equal(\"{escaped}\", content.Trim());");
    }

    // content_not_empty
    if a.content_not_empty == Some(true) {
        let _ = writeln!(out, "        Assert.False(string.IsNullOrWhiteSpace(content));");
    }

    // content_is_none
    if a.content_is_none == Some(true) {
        let _ = writeln!(out, "        Assert.True(string.IsNullOrWhiteSpace(content));");
    }

    // content_contains_all
    if let Some(substrings) = &a.content_contains_all {
        for s in substrings {
            let escaped = escape_csharp_string(s);
            let _ = writeln!(out, "        Assert.Contains(\"{escaped}\", content);");
        }
    }

    // content_contains_any
    if let Some(substrings) = &a.content_contains_any {
        let checks: Vec<String> = substrings
            .iter()
            .map(|s| format!("content.Contains(\"{}\")", escape_csharp_string(s)))
            .collect();
        let condition = checks.join(" || ");
        let _ = writeln!(
            out,
            "        Assert.True({condition}, \"expected content to contain at least one of the specified strings\");"
        );
    }

    // content_not_contains
    if let Some(substrings) = &a.content_not_contains {
        for s in substrings {
            let escaped = escape_csharp_string(s);
            let _ = writeln!(out, "        Assert.DoesNotContain(\"{escaped}\", content);");
        }
    }

    // min_content_length
    if let Some(min) = a.min_content_length {
        let _ = writeln!(
            out,
            "        Assert.True(content.Length >= {min}, $\"expected content length >= {min}, got {{content.Length}}\");"
        );
    }

    // max_content_length
    if let Some(max) = a.max_content_length {
        let _ = writeln!(
            out,
            "        Assert.True(content.Length <= {max}, $\"expected content length <= {max}, got {{content.Length}}\");"
        );
    }

    // content_starts_with
    if let Some(prefix) = &a.content_starts_with {
        let escaped = escape_csharp_string(prefix);
        let _ = writeln!(out, "        Assert.StartsWith(\"{escaped}\", content);");
    }

    // content_ends_with
    if let Some(suffix) = &a.content_ends_with {
        let escaped = escape_csharp_string(suffix);
        let _ = writeln!(out, "        Assert.EndsWith(\"{escaped}\", content);");
    }

    let _ = writeln!(out, "    }}");
}

/// Capitalize category name for C# class naming.
fn capitalize_category(s: &str) -> String {
    let mut out = String::new();
    let mut capitalize_next = true;

    for ch in s.chars() {
        if ch.is_ascii_alphanumeric() {
            if capitalize_next {
                out.push(ch.to_ascii_uppercase());
                capitalize_next = false;
            } else {
                out.push(ch.to_ascii_lowercase());
            }
        } else if ch == '_' || ch == '-' || ch == ' ' {
            capitalize_next = true;
        }
    }

    out
}

/// Convert a fixture id to a valid C# method name (PascalCase).
fn sanitize_method_name(s: &str) -> String {
    let mut out = String::new();
    let mut capitalize_next = true;

    for ch in s.chars() {
        if ch.is_ascii_alphanumeric() {
            if capitalize_next {
                out.push(ch.to_ascii_uppercase());
                capitalize_next = false;
            } else {
                out.push(ch.to_ascii_lowercase());
            }
        } else if ch == '_' || ch == '-' {
            capitalize_next = true;
        }
    }

    out
}

/// Escape a string for use inside a C# `"..."` string literal.
fn escape_csharp_string(s: &str) -> String {
    s.replace('\\', "\\\\")
        .replace('"', "\\\"")
        .replace('\n', "\\n")
        .replace('\r', "\\r")
        .replace('\t', "\\t")
}
