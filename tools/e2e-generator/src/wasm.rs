//! WebAssembly (WASM) test generator for html-to-markdown e2e tests.
//!
//! Generates `e2e/wasm/tests/{category}.test.ts` using vitest.

use crate::fixtures::Fixture;
use anyhow::Result;
use camino::Utf8Path;
use itertools::Itertools;
use std::collections::HashMap;
use std::fmt::Write as FmtWrite;

/// Generate WASM e2e tests from fixtures into `output_dir/wasm/`.
pub fn generate(fixtures: &[Fixture], output_dir: &Utf8Path) -> Result<usize> {
    let wasm_dir = output_dir.join("wasm");
    let tests_dir = wasm_dir.join("tests");
    std::fs::create_dir_all(&tests_dir)?;

    // Group fixtures by category.
    let by_category: HashMap<&str, Vec<&Fixture>> = fixtures
        .iter()
        .filter(|f| !f.is_skipped_for("wasm"))
        .into_group_map_by(|f| f.resolved_category());

    let mut total = 0;

    for (category, category_fixtures) in &by_category {
        let file_name = format!("{}.test.ts", sanitize_name(category));
        let path = tests_dir.join(&file_name);
        let content = render_test_file(category, category_fixtures);
        std::fs::write(&path, content)?;
        total += category_fixtures.len();
    }

    Ok(total)
}

fn render_test_file(category: &str, fixtures: &[&Fixture]) -> String {
    let mut out = String::new();
    let _ = writeln!(out, "/**");
    let _ = writeln!(out, " * E2e tests for category: {}", category);
    let _ = writeln!(
        out,
        " * Generated by html-to-markdown-e2e-generator — do not edit manually."
    );
    let _ = writeln!(out, " */");
    let _ = writeln!(out);
    let _ = writeln!(out, "import {{ describe, it, expect }} from 'vitest';");
    let _ = writeln!(out, "import {{ convert }} from '@kreuzberg/html-to-markdown-wasm';");
    let _ = writeln!(out);

    let _ = writeln!(out, "describe('{}', () => {{", category);

    for fixture in fixtures {
        render_test_function(&mut out, fixture, 2);
        let _ = writeln!(out);
    }

    let _ = writeln!(out, "}});");

    // Ensure final newline
    if !out.ends_with('\n') {
        out.push('\n');
    }
    out
}

fn render_test_function(out: &mut String, fixture: &Fixture, indent: usize) {
    let ind = " ".repeat(indent);
    let ind2 = " ".repeat(indent + 2);
    let description = &fixture.description;

    let _ = writeln!(out, "{}it('{}: {}', () => {{", ind, fixture.id, description);

    // HTML input
    match &fixture.html {
        Some(html) => {
            let escaped = escape_typescript_string(html);
            let _ = writeln!(out, "{}const html = `{}`;", ind2, escaped);
        }
        None => {
            let _ = writeln!(out, "{}it.skip('no inline html');", ind2);
            let _ = writeln!(out, "{}}});", ind);
            return;
        }
    }

    // Build options literal if specified.
    let options_literal = fixture.options.as_ref().map(|opts| {
        let entries: Vec<String> = opts
            .iter()
            .map(|(k, v)| {
                let js_val = json_value_to_js(v);
                format!("{k}: {js_val}")
            })
            .collect();
        format!("{{ {} }}", entries.join(", "))
    });

    let convert_arg = if let Some(ref lit) = options_literal {
        format!("html, {lit}")
    } else {
        "html".to_string()
    };

    // Conversion call
    if fixture.assertions.expect_error == Some(true) {
        let _ = writeln!(out, "{}expect(() => convert({})).toThrow();", ind2, convert_arg);
        if let Some(contains) = &fixture.assertions.error_contains {
            let escaped = escape_typescript_string(contains);
            let _ = writeln!(out, "{}// Error should contain: {}", ind2, escaped);
        }
        let _ = writeln!(out, "{}}});", ind);
        return;
    }

    let _ = writeln!(out, "{}const result = convert({});", ind2, convert_arg);
    let _ = writeln!(out, "{}const content = result.content ?? '';", ind2);
    let _ = writeln!(out);

    let a = &fixture.assertions;

    // content_equals
    if let Some(expected) = &a.content_equals {
        let escaped = escape_typescript_string(expected);
        let _ = writeln!(out, "{}expect(content.trim()).toBe('{}');", ind2, escaped);
    }

    // content_not_empty
    if a.content_not_empty == Some(true) {
        let _ = writeln!(out, "{}expect(content.trim().length).toBeGreaterThan(0);", ind2);
    }

    // content_is_none
    if a.content_is_none == Some(true) {
        let _ = writeln!(out, "{}expect(content.trim().length).toBe(0);", ind2);
    }

    // content_contains_all
    if let Some(substrings) = &a.content_contains_all {
        for s in substrings {
            let escaped = escape_typescript_string(s);
            let _ = writeln!(out, "{}expect(content).toContain('{}');", ind2, escaped);
        }
    }

    // content_contains_any
    if let Some(substrings) = &a.content_contains_any {
        let _ = writeln!(out, "{}// content_contains_any", ind2);
        let checks: Vec<String> = substrings
            .iter()
            .map(|s| format!("content.includes('{}')", escape_typescript_string(s)))
            .collect();
        let condition = checks.join(" || ");
        let _ = writeln!(out, "{}expect({}as boolean).toBe(true);", ind2, condition);
    }

    // content_not_contains
    if let Some(substrings) = &a.content_not_contains {
        for s in substrings {
            let escaped = escape_typescript_string(s);
            let _ = writeln!(out, "{}expect(content).not.toContain('{}');", ind2, escaped);
        }
    }

    // min_content_length
    if let Some(min) = a.min_content_length {
        let _ = writeln!(out, "{}expect(content.length).toBeGreaterThanOrEqual({});", ind2, min);
    }

    // max_content_length
    if let Some(max) = a.max_content_length {
        let _ = writeln!(out, "{}expect(content.length).toBeLessThanOrEqual({});", ind2, max);
    }

    // content_starts_with
    if let Some(prefix) = &a.content_starts_with {
        let escaped = escape_typescript_string(prefix);
        let _ = writeln!(
            out,
            "{}expect(content.trim()).toMatch(/^{}/);",
            ind2,
            escape_regex(&escaped)
        );
    }

    // content_ends_with
    if let Some(suffix) = &a.content_ends_with {
        let escaped = escape_typescript_string(suffix);
        let _ = writeln!(
            out,
            "{}expect(content.trim()).toMatch(/{}$/);",
            ind2,
            escape_regex(&escaped)
        );
    }

    let _ = writeln!(out, "{}}});", ind);
}

/// Convert a string to a valid TypeScript identifier.
fn sanitize_name(s: &str) -> String {
    let mut out = String::with_capacity(s.len());
    for ch in s.chars() {
        if ch.is_ascii_alphanumeric() || ch == '_' {
            out.push(ch);
        } else {
            out.push('_');
        }
    }
    // Remove leading digits
    if out.starts_with(|c: char| c.is_ascii_digit()) {
        out.insert(0, '_');
    }
    out
}

/// Format a `serde_json::Value` as a JavaScript literal.
fn json_value_to_js(v: &serde_json::Value) -> String {
    match v {
        serde_json::Value::String(s) => format!("'{}'", escape_js_string(s)),
        serde_json::Value::Bool(b) => b.to_string(),
        serde_json::Value::Number(n) => n.to_string(),
        serde_json::Value::Null => "null".to_string(),
        other => format!("'{}'", escape_js_string(&other.to_string())),
    }
}

/// Escape a string for use inside a JavaScript `'...'` string literal.
fn escape_js_string(s: &str) -> String {
    s.replace('\\', "\\\\")
        .replace('\'', "\\'")
        .replace('\n', "\\n")
        .replace('\r', "\\r")
        .replace('\t', "\\t")
}

/// Escape a string for use inside a TypeScript template literal `` `...` ``.
fn escape_typescript_string(s: &str) -> String {
    s.replace('\\', "\\\\").replace('`', "\\`").replace('$', "\\$")
}

/// Escape special regex characters for use in a regex pattern.
fn escape_regex(s: &str) -> String {
    s.replace('\\', "\\\\")
        .replace('.', "\\.")
        .replace('^', "\\^")
        .replace('$', "\\$")
        .replace('*', "\\*")
        .replace('+', "\\+")
        .replace('?', "\\?")
        .replace('(', "\\(")
        .replace(')', "\\)")
        .replace('[', "\\[")
        .replace(']', "\\]")
        .replace('{', "\\{")
        .replace('}', "\\}")
        .replace('|', "\\|")
}
