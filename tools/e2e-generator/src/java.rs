fn render_test_file(class_name: &str, category: &str, fixtures: &[&Fixture]) -> String {
    let mut out = String::new();
    let _ = writeln!(out, "// Code generated by html-to-markdown-e2e-generator; DO NOT EDIT.");
    let _ = writeln!(out);
    let _ = writeln!(out, "// E2e tests for category: {category}");
    let _ = writeln!(out, "package dev.kreuzberg.e2e;");
    let _ = writeln!(out);
    let _ = writeln!(out, "import static org.junit.jupiter.api.Assertions.*;");
    let _ = writeln!(out);
    let _ = writeln!(out, "import org.junit.jupiter.api.Test;");
    let _ = writeln!(out, "import dev.kreuzberg.htmltomarkdown.HtmlToMarkdown;");
    let _ = writeln!(out, "import dev.kreuzberg.htmltomarkdown.ConversionResult;");
    let _ = writeln!(out);
    let _ = writeln!(out, "class {class_name}Test {{");
    let _ = writeln!(out);

    for fixture in fixtures {
        render_test_function(&mut out, fixture);
        let _ = writeln!(out);
    }

    let _ = writeln!(out, "}}");

    out
}

fn render_test_function(out: &mut String, fixture: &Fixture) {
    let fn_name = sanitize_method_name(&fixture.id);
    let description = &fixture.description;

    // HTML input.
    let html = match &fixture.html {
        Some(html) => html.as_str(),
        None => {
            let _ = writeln!(out, "    @Test");
            let _ = writeln!(out, "    void test{fn_name}() {{");
            let _ = writeln!(out, "        // {description}");
            let _ = writeln!(out, "        // SKIP: no inline html");
            let _ = writeln!(out, "    }}");
            return;
        }
    };

    let escaped_html = escape_java_string(html);

    // Conversion call + error handling.
    if fixture.assertions.expect_error == Some(true) {
        let _ = writeln!(out, "    @Test");
        let _ = writeln!(out, "    void test{fn_name}() {{");
        let _ = writeln!(out, "        // {description}");
        let _ = writeln!(out, "        var html = \"{escaped_html}\";");
        let _ = writeln!(
            out,
            "        assertThrows(Exception.class, () -> HtmlToMarkdown.convert(html));"
        );
        if let Some(contains) = &fixture.assertions.error_contains {
            let escaped = escape_java_string(contains);
            let _ = writeln!(
                out,
                "        var ex = assertThrows(Exception.class, () -> HtmlToMarkdown.convert(html));"
            );
            let _ = writeln!(out, "        assertTrue(ex.getMessage().contains(\"{escaped}\"));");
        }
        let _ = writeln!(out, "    }}");
        return;
    }

    let _ = writeln!(out, "    @Test");
    let _ = writeln!(out, "    void test{fn_name}() {{");
    let _ = writeln!(out, "        // {description}");
    let _ = writeln!(out, "        var html = \"{escaped_html}\";");
    let _ = writeln!(out, "        var result = HtmlToMarkdown.convert(html);");
    let _ = writeln!(
        out,
        "        var content = result.content() != null ? result.content() : \"\";"
    );
    let _ = writeln!(out);

    let a = &fixture.assertions;

    // content_equals
    if let Some(expected) = &a.content_equals {
        let escaped = escape_java_string(expected);
        let _ = writeln!(
            out,
            "        assertEquals(\"{escaped}\", content.strip(), \"content_equals mismatch\");"
        );
    }

    // content_not_empty
    if a.content_not_empty == Some(true) {
        let _ = writeln!(
            out,
            "        assertNotEquals(\"\", content.strip(), \"expected non-empty content\");"
        );
    }

    // content_is_none
    if a.content_is_none == Some(true) {
        let _ = writeln!(
            out,
            "        assertEquals(\"\", content.strip(), \"expected empty/null content\");"
        );
    }

    // content_contains_all
    if let Some(substrings) = &a.content_contains_all {
        for s in substrings {
            let escaped = escape_java_string(s);
            let _ = writeln!(
                out,
                "        assertTrue(content.contains(\"{escaped}\"), \"expected content to contain: {escaped}\");"
            );
        }
    }

    // content_contains_any
    if let Some(substrings) = &a.content_contains_any {
        let checks: Vec<String> = substrings
            .iter()
            .map(|s| format!("content.contains(\"{}\")", escape_java_string(s)))
            .collect();
        let condition = checks.join(" || ");
        let _ = writeln!(
            out,
            "        assertTrue({condition}, \"expected content to contain at least one of the specified strings\");"
        );
    }

    // content_not_contains
    if let Some(substrings) = &a.content_not_contains {
        for s in substrings {
            let escaped = escape_java_string(s);
            let _ = writeln!(
                out,
                "        assertFalse(content.contains(\"{escaped}\"), \"expected content NOT to contain: {escaped}\");"
            );
        }
    }

    // min_content_length
    if let Some(min) = a.min_content_length {
        let _ = writeln!(
            out,
            "        assertTrue(content.length() >= {min}, \"expected content length >= {min}, got \" + content.length());"
        );
    }

    // max_content_length
    if let Some(max) = a.max_content_length {
        let _ = writeln!(
            out,
            "        assertTrue(content.length() <= {max}, \"expected content length <= {max}, got \" + content.length());"
        );
    }

    // content_starts_with
    if let Some(prefix) = &a.content_starts_with {
        let escaped = escape_java_string(prefix);
        let _ = writeln!(
            out,
            "        assertTrue(content.startsWith(\"{escaped}\"), \"expected content to start with: {escaped}\");"
        );
    }

    // content_ends_with
    if let Some(suffix) = &a.content_ends_with {
        let escaped = escape_java_string(suffix);
        let _ = writeln!(
            out,
            "        assertTrue(content.endsWith(\"{escaped}\"), \"expected content to end with: {escaped}\");"
        );
    }

    let _ = writeln!(out, "    }}");
}

/// Convert a category name to a valid Java class name (PascalCase).
fn category_to_class_name(s: &str) -> String {
    let mut out = String::new();
    let mut capitalize_next = true;

    for ch in s.chars() {
        if ch.is_ascii_alphanumeric() {
            if capitalize_next {
                out.push(ch.to_ascii_uppercase());
                capitalize_next = false;
            } else {
                out.push(ch);
            }
        } else if ch == '_' || ch == '-' || ch == ' ' {
            capitalize_next = true;
        }
    }

    out
}

/// Convert a fixture id to a valid Java method name suffix (PascalCase for use after "test" prefix).
///
/// Example: "smoke_empty_string" -> "SmokeEmptyString" (caller emits "testSmokeEmptyString")
fn sanitize_method_name(s: &str) -> String {
    let mut out = String::new();
    let mut capitalize_next = true;

    for ch in s.chars() {
        if ch.is_ascii_alphanumeric() {
            if capitalize_next {
                out.push(ch.to_ascii_uppercase());
                capitalize_next = false;
            } else {
                out.push(ch);
            }
        } else if ch == '_' || ch == '-' || ch == ' ' {
            capitalize_next = true;
        }
    }

    out
}

/// Escape a string for use inside a Java string literal.
fn escape_java_string(s: &str) -> String {
    s.replace('\\', "\\\\")
        .replace('"', "\\\"")
        .replace('\n', "\\n")
        .replace('\r', "\\r")
        .replace('\t', "\\t")
}
